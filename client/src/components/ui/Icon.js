/**
 * Icon Component
 * 
 * @param {string} name - Icon name from the Icons library
 * @param {string} size - Icon size: 'xs' | 'sm' | 'md' | 'lg' | 'xl'
 * @param {string} className - Additional CSS classes
 */
export const Icon = ({ 
  name, 
  size = 'md', 
  className = '', 
  ...props 
}) => {
  const sizes = {
    xs: 'h-3 w-3',
    sm: 'h-4 w-4',
    md: 'h-5 w-5',
    lg: 'h-6 w-6',
    xl: 'h-8 w-8',
    '2xl': 'h-10 w-10',
  };

  const IconComponent = Icons[name];
  
  if (!IconComponent) {
    console.warn(`Icon "${name}" not found`);
    return null;
  }

  return (
    <IconComponent 
      className={`${sizes[size]} ${className}`}
      {...props}
    />
  );
};

